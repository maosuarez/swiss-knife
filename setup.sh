echo ""
echo "\033[33mв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—"
echo "в•‘       рџ”Є  Swiss Knife вЂ” Setup                    в•‘"
echo "в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\033[0m"
echo ""

# 1. Dependencias de sistema
echo "\033[36m[1/4] Verificando dependencias del sistema...\033[0m"

# в”Ђв”Ђ ffmpeg в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if ! command -v ffmpeg &>/dev/null; then
    echo "  вљ  ffmpeg no encontrado. Instalando..."
    sudo apt update -qq && sudo apt install -y ffmpeg
else
    echo "  вњ“ ffmpeg OK"
fi

# в”Ђв”Ђ LibreOffice в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if ! command -v libreoffice &>/dev/null; then
    echo "  вљ  LibreOffice no encontrado. Instalando..."
    sudo apt update -qq && sudo apt install -y libreoffice
else
    echo "  вњ“ LibreOffice OK"
fi

# в”Ђв”Ђ Pandoc в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if ! command -v pandoc &>/dev/null; then
    echo "  вљ  Pandoc no encontrado. Instalando..."
    sudo apt update -qq && sudo apt install -y pandoc
else
    echo "  вњ“ Pandoc OK"
fi

# 2. Crear .venv
echo ""
echo "\033[36m[2/4] Configurando entorno virtual...\033[0m"
if [ ! -d "$VENV_DIR" ]; then
    python3 -m venv "$VENV_DIR"
    echo "  вњ“ .venv creado en $VENV_DIR"
else
    echo "  вњ“ .venv ya existe"
fi

# 3. Instalar dependencias Python
echo ""
echo "\033[36m[3/4] Instalando dependencias Python...\033[0m"
"$VENV_DIR/bin/pip" install --upgrade pip -q
"$VENV_DIR/bin/pip" install -r "$KNIFE_DIR/requirements.txt"
