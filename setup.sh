if grep -q "$MARKER" "$PROFILE_FILE" 2>/dev/null; then
    # Reemplazar alias existente
    sed -i "/$MARKER/d" "$PROFILE_FILE"
    echo "  в†є Alias actualizado"
fi

echo "" >> "$PROFILE_FILE"
echo "$MARKER" >> "$PROFILE_FILE"
echo "$ALIAS_LINE" >> "$PROFILE_FILE"
echo "  вњ“ Alias registrado"

echo ""
echo "\033[32mв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
echo "  вњ…  InstalaciГіn completada."
echo ""
echo "  Ejecuta:  source $PROFILE_FILE"
echo "  Luego:    knife --list"
echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\033[0m"
